# When it is 20C. Calculation and print.
CELSIUS = 20
fahrenheit = 1.8 * CELSIUS + 32
print("Celsius Fahrenheit")
print(format(CELSIUS, "4d"), end="")
print(format(fahrenheit, "11.1f"))

